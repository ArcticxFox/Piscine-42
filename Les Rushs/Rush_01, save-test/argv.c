int	parse_vals(char *s, int out[16])
{
	int	r;

	r = 0;
	while (*s)
	{
		while (*s == ' ' || *s == '\t')
			s++;
		if (*s == '\0')
			break ;
		if (*s < '1' || *s > '4')
			return (-1);
		if (r >= 16)
			return (-1);
		out[r++] = *s - '0';
		s++;
	}
	if (r == 16)
		return (0);
	else
		return (-1);
}
