#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	error_msg(char *msg)
{
	while (*msg)
	{
		write(1, msg, 1);
		msg++;
	}
	write(1, "/n", 1);
}
